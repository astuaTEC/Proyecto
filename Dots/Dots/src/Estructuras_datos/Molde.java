package Estructuras_datos;

public class Molde {
	
	private Object salida_cliente;
		
	private Object llegada_Servidor;
	
	private int jugador;
	
	private int Scores[];

	public Object getSalida_cliente() {
		return salida_cliente;
	}

	public void setSalida_cliente(Object salida_cliente) {
		this.salida_cliente = salida_cliente;
	}

	public Object getLlegada_Servidor() {
		return llegada_Servidor;
	}

	public void setLlegada_Servidor(Object llegada_Servidor) {
		this.llegada_Servidor = llegada_Servidor;
	}

	public int getJugador() {
		return jugador;
	}

	public void setJugador(int jugador) {
		this.jugador = jugador;
	}

	public int[] getScores() {
		return Scores;
	}

	public void setScores(int[] scores) {
		Scores = scores;
	}
	
	
	
	



}
